/******************************************************************************/
/*                              PRIVATE DATA                                  */
/******************************************************************************/

const char *TAG = "MAIN";
static uint32_t cycles = 0;

/******************************************************************************/
/*                              EXPORTED DATA                                 */
/******************************************************************************/



/******************************************************************************/
/*                                FUNCTIONS                                   */
/******************************************************************************/



/******************************************************************************/

void setup() {
    /* Enable debug logging */
    esp_log_level_set("*", ESP_LOG_INFO);
    delay(1000);
    ESP_LOG_LEVEL(ESP_LOG_INFO, TAG, "Power up");
    delay(1000);

    /* Initialize LCD and touch */
    lcd_gui_init();
}

void loop() {
    delay(1000);
    ESP_LOG_LEVEL(ESP_LOG_INFO, TAG, "Main cycles %u", cycles++);
}
